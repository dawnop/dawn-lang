// Turn `dawn doc --builtins` output into a typed completion table baked into the
// bundle. Run by prebuild/predev. Reads site/build/builtins.json when present
// (build.sh produces it first); falls back to regenerating it via the dawn CLI
// so `npm run dev` works standalone.
import { execFileSync } from 'node:child_process'
import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const repoRoot = resolve(here, '../../..')
const buildJson = resolve(repoRoot, 'site/build/builtins.json')
const outFile = resolve(here, '../src/builtins.generated.ts')

function loadBuiltins() {
  if (existsSync(buildJson)) return JSON.parse(readFileSync(buildJson, 'utf8'))
  const raw = execFileSync(resolve(repoRoot, 'bin/dawn'), ['doc', '--builtins'], {
    encoding: 'utf8',
    maxBuffer: 8 * 1024 * 1024,
  })
  return JSON.parse(raw)
}

const data = loadBuiltins()
const fns = []
for (const group of data.groups ?? []) {
  for (const fn of group.fns ?? []) {
    fns.push({ name: fn.name, sig: fn.sig, doc: fn.doc ?? '', group: group.name })
  }
}
fns.sort((a, b) => a.name.localeCompare(b.name))

const banner = `// GENERATED by scripts/gen-builtins.mjs from \`dawn doc --builtins\`. Do not edit.\n`
const body =
  `export interface Builtin { name: string; sig: string; doc: string; group: string }\n` +
  `export const BUILTINS: Builtin[] = ${JSON.stringify(fns, null, 2)}\n`

mkdirSync(dirname(outFile), { recursive: true })
writeFileSync(outFile, banner + body)
console.log(`gen-builtins: wrote ${fns.length} builtins to ${outFile}`)
